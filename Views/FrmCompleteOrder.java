    private void addOrder(Order order){
        Object[] dataRow=createOrderTableRow(order);
        tableModelOrders.addRow(dataRow);
    }
    
    private Object[] createOrderTableRow(Order order){
        int id = order.getId();
        return new Object[] {id};
    }
    
    private void addProducts(Order order){
        Iterator<Product> iterator = order.getProducts();
        while (iterator.hasNext()){
            Product product = iterator.next();
            Object[] dataRow=createProductTableRow(order,product);
            tableModelProducts.addRow(dataRow);
        }
    }
    
    private Object[] createProductTableRow(Order order,Product product){
        String description = product.getDescription();
        int quantity = order.getQuantity(product);
        return new Object[] {description,quantity};
    }
    
    private void removeOrder(Order order){
        int rowCount = tableModelOrders.getRowCount();
        for (int r=0;r<rowCount;r++){
            int id = Integer.parseInt(tableModelOrders.getValueAt(r, 0).toString());
            if (id==order.getId()){
                tableModelOrders.removeRow(r);
                tableModelProducts.setRowCount(0);
            }
        }
    }

